//La diferencia entre parámetros y argumentos

function nombreCompleto (nombre, apellido) { //Los parámetros son lo que la función recibe cuando la estamos creando
    return nombre + ' ' + apellido;
}

nombreCompleto ('Mario', 'Yanez'); //Los argumentos son los datos que le damos a la función cuando la mandamos a llamar.

